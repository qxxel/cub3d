#include "cub3d.h"

void	check_empty(char **map, int *i)
{
	int	j;

	while (map[*i])
	{
		j = 0;
		if (map[*i][0] == ' ')
		{
			while (map[*i][j] == ' ')
				j++;
		}
		if (map[*i][j] != '\0')
			return ;
		(*i)++;
	}
}
